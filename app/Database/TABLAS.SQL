CREATE TABLE PERSONA (
    ID_REGISTRO_PERSONA varchar(50) NOT NULL,
    ID_PERSONA varchar(30) NOT NULL,
    NOMBRE varchar(30) NOT NULL,
    APPELLIDOS varchar(30) NOT NULL,
    FECH_NACIMIENTO date NOT NULL,
    GENERO char(2) NOT NULL,
    FK1_PERSONA_UBICACION varchar(30) NOT NULL,
    FK2_PERSONA_CONTACTO varchar(30) NOT NULL,
    FK3_TIPO_PERSONA varchar(30) NOT NULL,
    FK4_PASSWORD_PERSONA VARCHAR(30)
) ;
#LLAVE PK
ALTER TABLE PERSONA ADD PRIMARY KEY (ID_REGISTRO_PERSONA);
#LLAVES FK
ALTER TABLE PERSONA ADD CONSTRAINT PERSONA_IBFK_1 FOREIGN KEY (FK1_PERSONA_UBICACION) REFERENCES PERSONA_UBICACION (ID_UBICACION);
ALTER TABLE PERSONA ADD CONSTRAINT PERSONA_IBFK_2 FOREIGN KEY (FK2_PERSONA_CONTACTO) REFERENCES PERSONA_CONTACTO (ID_CONTACTO);
ALTER TABLE PERSONA ADD CONSTRAINT PERSONA_IBFK_3 FOREIGN KEY (FK3_TIPO_PERSONA) REFERENCES TIPO_PERSONA (ID_TIPO);
ALTER TABLE PERSONA ADD CONSTRAINT PERSONA_IBFK_4 FOREIGN KEY(FK4_PASSWORD_PERSONA) REFERENCES PASSWORD_PERSONA (ID_PASSWORD);
-- --------------------------------------------------------

